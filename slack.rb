Setting UP

1. Clone from slate app and change path name
2. Change Ruby version higher than 2.4 and above
3. Bundle install
4. Click button (Depiloy to Heroku) on github readme page
5. Create new app in heroku and deploy.

Update and Changes to app.

1. Edit and update done on the codebase api
2. Git push to repo
3. Find the app which caters to the code from heroku (copy name and destroy instance)
4. Click button (Depiloy to Heroku) on github readme page
5. Create new app in heroku and deploy. (naming of app should be the same from last destroyed instance)
